    private void checkServer() {
        try {
            ServerConnector serverConnector = ServerConnector.getServerConnector();
        } catch (NotBoundException | MalformedURLException | RemoteException ex) {
            new ServerNull().setVisible(true);
            this.dispose();
            setVisible(false);
        }
    }

    private void checkpassword() {
        try {
            UserController userController = ServerConnector.getServerConnector().getUserController();
            Users searchUser = userController.searchUser(txtUserName.getText());
            if (searchUser != null) {
                if (searchUser.getUserPassword().equals(txtPassword.getText().toString())) {
                    new HomePage().setVisible(true);
                    this.dispose();
                } else if (txtUserName.getText().equals("") || txtPassword.getText().toString().equals("")) {
                    JOptionPane.showMessageDialog(this, "Yuor password or username invalid !!");
                } else {
                    JOptionPane.showMessageDialog(this, "Yuor password or username invalid !!");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Yuor password or username invalid !!");
            }
        } catch (NotBoundException | MalformedURLException | RemoteException ex) {
            new ServerNull().setVisible(true);
        } catch (ClassNotFoundException | IOException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
